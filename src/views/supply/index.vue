    一、相关概念

    （一）大宗商品

    本文所提到的大宗商贸公司即以大宗商品交易为主要业务范围的公司。大宗商品是指可进入流通领域、但非零售环节，具有商品属性，用于工农业生产与消费使用的大批量买卖的物资商品。大宗商品分为能源商品、农副产品和基础原材料三类，这三类商品都具有同质化、可交易等属性，我们常提到的原油、有色金属、钢铁、农产品、铁矿石、煤炭等，都是大宗商品。

    （二）供应链金融

    供应链金融是供应链管理的外延，最初由深圳发展银行（现已被平安银行兼并）提出。它是以核心企业为依托，以供应链条上下游企业为服务对象，以企业间真实交易为基础的分配金融资源的活动模式。这一模式打破了以往银行只针对某一单一企业的经营状况进行评估授信的融资方式，转而把供应链上的各个企业结合在一起，作为整体来看待，是对融资模式和渠道的创新。供应链上的所有企业都可以分享资源，中小企业也可以参与进来。

    二、国内供应链金融发展现状

    截至2021年年底，我国市场主体数量已超过1.4亿个，其中99%以上是中小微企业（含个体工商户）。中小微企业在GDP贡献、提供就业、贡献税收、技术创新等方面早已成为重要力量，是我国经济不可忽视的重要组成部分，因此国家也尤其注重中小微企业的发展。中小微企业在发展过程中遇到的融资难问题，也得到了很大重视。供应链金融能够在一定程度上缓解中小微企业融资难问题，因此政府高度重视，出台一系列政策，鼓励和支持发展供应链金融。

    供应链金融虽然起步较晚，但是发展速度很快。据专业人士介绍，2020年，仅供应链金融上游市场的规模已超过12万亿元，其下游场景规模接近20万亿元，也就是说2020年年末，供应链金融市场规模已超30万亿元。

    现阶段，国内多家上市企业均在部署供应链金融业务。据同花顺软件统计数据，供应链金融板块概念股数量约有50个。2022年，新年伊始，众企安链[由众安在线（HK6060）孵化]宣布已拿下数额高达千万元人民币的A轮融资，成为2022年第一个拿下融资的供应链金融企业。可见，国内供应链金融发展正如火如荼。

    三、大宗商贸企业选供应链金融作为融资渠道的原因

    （一）经营模式原因

    大宗商贸公司的经营模式基本是“采购—销售”或者中间加一步短期的储存。从采购到销售的时间一般较短，但是发货和回款的时间一般较长。“两金”（预付、应收和存货）占用了大量资金，资金短缺导致企业无法继续开展其他业务，降低了资金利用的效率，也限制了企业的发展。另外，由于大宗商品贸易企业大多处于轻资产状态，因此无法给银行提供有效的资产抵押，也无法获得银行的资金支持。而供应链金融服务恰好可以有效解决这一难题。

    （二）资金安全性及融资难度原因

    为了防范资金风险，大宗商贸公司一般会选择大型的、信用较好的企业合作，所以上下游企业一般为央企或者大型国企，这种类型的企业有雄厚的资产托底，且因一定的政治关联，一般信用较好。

    相比于大企业对中小企业的应收账款，供应链金融服务机构似乎更钟情于中小企业对大型企业的应收账款，因此供应链金融实际在帮助中小企业融资方面起到了举足轻重的作用。中小企业由于规模、现金流及资产数量及质量等影响，较难以传统的融资方式———从银行贷款获取资金。而供应链金融恰好可以以应收账款作为标的，帮助中小企业获取资金，帮助大型企业资金周转；另外，也为金融机构提供了众多的客户资源和广泛的业务基础。可以说，利用供应链金融是一种“多赢”的方式。但是这种帮中小企业获取资金的方式，也仅限于基于交易产权的资金周转，无法帮助中小企业扩大规模，形成竞争优势。

    （三）弱势地位原因

    主观上来讲，大企业对大宗商贸公司的应付账款，因其在交易中的较强话语权，和对信用销售的青睐，而不愿意早点支付。位于劣势地位的大宗商贸公司只好寻求应收账款债券的转让来实现资金的回笼。客观上来讲，一些大宗贸易公司，虽有实力雄厚的企业集团母公司作为后盾，但是目前这些集团大多资产负债率非常高，银行授信多已达上限，基本都是处于还款后重新贷款的模式。而重新贷款，银行很有可能提高利率。由于疲于应付前期贷款及利息的刚性兑付，企业难以保障资金调度与业务周期的匹配性。而供应链金融恰好可以灵活地起到资金周转的作用。

    （四）商贸供应链金融本身优势

    除了大宗商贸公司本身的特质，商贸板块供应链金融的平稳有序健康发展态势，也成为大宗商贸公司选择供应链金融的重要原因。

    同时，供应链金融能够巩固核心企业的采购和销售渠道，也能够维持弱势非核心企业的正常经营，保障产品的销售。因此受到各方青睐。

