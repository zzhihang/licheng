    大宗商贸公司担心资金安全问题，保理公司同样担心大宗商贸公司或者交易链条上的某些公司“爆雷”，导致资金损失。因此，保理公司在选择业务对象时，常常以企业集团为依托，为的就是找个托底的，即便是有公司爆雷，也有集团母公司来完成资金的支付。本文以大型企业集团为例，讲述应收账款资产证券化、保理实际操作过程以及需要注意的问题。因此有必要介绍一下企业集团供应链金融。

    社会改革不断深化，国企混改持续推进，集团与集团的竞争、供应链与供应链的竞争，已经取代了以往单一企业之间的竞争。在混改的政策引导下，“国进民退”是大趋势，民营企业逐渐被国有企业兼并收购，国有大型企业规模不断扩大。甚至大型企业集团之间也时不时就进行重组，比如中国南车和中国北车、山东高速与齐鲁交通、山东能源与兖矿集团。企业集团中的企业类型各不相同，发展阶段各有差异，发展层次也参差不齐。新成立公司由于其初创期，难以通过融资获得资金，对母公司在资金方面有较强的依赖性，对融资有较强的需求。另外就是公司的业务模式导致其无法通过传统模式获得贷款，比如大宗商贸公司。

