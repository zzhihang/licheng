    五、大宗商贸公司供应链金融存在的问题

    （一）模式单一，业务覆盖面较窄

    我国的供应链金融兴起时间较短，目前来说，商业银行仅针对汽车、能源等大型产业提供供应链金融业务，而对于相对较小的业务较少提供对应的产品和服务。其他非银行金融机构，如保理公司，虽对诸如大豆、玉米等大宗商品公司提供供应链金融，也较多选择企业集团背景的大宗商贸公司，其他中小企业的融资难问题，依然无法得到有效改观。

    传统模式仍占据主流地位，对资产较为看重，以应收、预付账款以及存货融资为主，而以订单、入库单为标的的融资方式，以及互联网融资方式还鲜少见到。商贸流通大数据平台尚未建立，互联网金融在商贸企业的应用还不深入，各大电商平台的供应链金融服务还有很大的进步空间。

    另外，所有的供应链金融，现阶段仅能够应用于国内贸易，对于动辄几千万元的国际贸易，无法提供服务，也无法保障资金的安全性。这块业务，尚需要进一步研究，以为我国的国际贸易企业的发展保驾护航，提供更多的资金支持。

    （二）发展不均衡，违约情况屡有发生

    从全国来看，供应链金融大多集中应用于沿海地区，而中西部地区则运用得较少，呈现发展不均衡的态势。

    因中西部企业发展阶段存在一定差异，内控严宽程度不一，有些企业对供应链管理的意识不强，由核心企业“兜底”的模式因缺乏激励，核心企业缺乏积极性。另外，有些核心企业过度担保或者自身经营出现问题，会导致违约，使供应链的声誉受损，可能会给进一步推行供应链金融带来不利的影响。

    （三）制度不完善且尚未形成统一的行业标准

    虽然国际上供应链金融已很成熟，但是我国供应链金融兴起时间较短，诸多内容无法照搬，需要结合我国实际情况来修正，政府、金融机构以及企业都在摸索。法律法规尚不完善，政策支持还存在漏洞，业务指导也不具有规范和统一性；且供应链金融的风险点很多，风控难度较大，相关人才也比较缺乏。在业务实操过程中，往往出现各家金融机构在资产质押内容以及资料审核方面存在较大差异，给企业带来一定困扰，且增加工作量。不仅如此，由于对风险把控的力度不够，还容易给各主体带来风险。
